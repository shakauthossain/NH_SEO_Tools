def get_super_admin(credentials: HTTPBasicCredentials = Depends(security_basic)):
    correct_username = os.getenv("ADMIN_EMAIL", "admin@seo.com")
    correct_password = os.getenv("ADMIN_PASSWORD", "supersecureadmin123")
    
